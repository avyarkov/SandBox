package _sandbox_;

public class MathFloorMod {
    public static void main(String[] args) {
        System.out.println("7 % 5 = " + 7 % 5);
        System.out.println("-7 % 5 = " + -7 % 5);

        System.out.println("Math.floorMod(7, 5) = " + Math.floorMod(7, 5));
        System.out.println("Math.floorMod(-7, 5) = " + Math.floorMod(-7, 5));

        System.out.println("-------");

        System.out.println("7 / 5 = " + 7 / 5);
        System.out.println("-7 / 5 = " + -7 / 5);

        System.out.println("Math.floorDiv(7, 5) = " + Math.floorDiv(7, 5));
        System.out.println("Math.floorDiv(-7, 5) = " + Math.floorDiv(-7, 5));

    }
}
